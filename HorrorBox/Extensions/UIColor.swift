import UIKit

extension UIColor {
    static var blackBackground: UIColor = {
        return UIColor(displayP3Red: 38/255, green: 38/255, blue: 38/255, alpha: 1)
    }()
    
    static var grayMovieBackground: UIColor = {
        return UIColor(displayP3Red: 216/255, green: 216/255, blue: 216/255, alpha: 0.3)
    }()
}
